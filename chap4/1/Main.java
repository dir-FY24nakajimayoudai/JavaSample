public class Main {
    public static void main(String[] args) {
        int a = 1;
        while (a < 5) {
            System.out.print(a);
            ++a;
        }
        System.out.println("\n--------");
        int b = 1;
        while (b <= 5) {
            b++;
            System.out.print(b);
        }
    }
}
